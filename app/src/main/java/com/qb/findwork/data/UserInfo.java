public class UserInfo {
    private String UserInfoIcon;
    private String UserInfoName;
    private String UserInfoSex;
    private String UserInfoAge;

    public String getUserInfoIcon() {
        return UserInfoIcon;
    }

    public void setUserInfoIcon(String userInfoIcon) {
        UserInfoIcon = userInfoIcon;
    }

    private String UserInfoPhone;

    public String getUserInfoName() {
        return UserInfoName;
    }

    public void setUserInfoName(String userInfoName) {
        UserInfoName = userInfoName;
    }

    public String getUserInfoSex() {
        return UserInfoSex;
    }

    public void setUserInfoSex(String userInfoSex) {
        UserInfoSex = userInfoSex;
    }

    public String getUserInfoAge() {
        return UserInfoAge;
    }

    public void setUserInfoAge(String userInfoAge) {
        UserInfoAge = userInfoAge;
    }

    public String getUserInfoPhone() {
        return UserInfoPhone;
    }

    public void setUserInfoPhone(String userInfoPhone) {
        UserInfoPhone = userInfoPhone;
    }
}
